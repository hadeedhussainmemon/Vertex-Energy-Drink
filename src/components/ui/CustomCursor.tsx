"use client";

import { useEffect, useState } from "react";
import { motion } from "framer-motion";

export default function CustomCursor() {
    const [mousePosition, setMousePosition] = useState({ x: 0, y: 0 });
    const [isHovering, setIsHovering] = useState(false);

    useEffect(() => {
        const updateMousePosition = (e: MouseEvent) => {
            setMousePosition({ x: e.clientX, y: e.clientY });
        };

        const handleMouseOver = (e: MouseEvent) => {
            if ((e.target as HTMLElement).tagName === 'BUTTON' || (e.target as HTMLElement).tagName === 'A') {
                setIsHovering(true);
            } else {
                setIsHovering(false);
            }
        };

        window.addEventListener("mousemove", updateMousePosition);
        window.addEventListener("mouseover", handleMouseOver);

        return () => {
            window.removeEventListener("mousemove", updateMousePosition);
            window.removeEventListener("mouseover", handleMouseOver);
        };
    }, []);

    return (
        <>
            <motion.div
                className="fixed top-0 left-0 w-4 h-4 bg-neon-blue rounded-full pointer-events-none z-[100] mix-blend-difference"
                animate={{ x: mousePosition.x - 8, y: mousePosition.y - 8, scale: isHovering ? 2 : 1 }}
                transition={{ type: "tween", ease: "backOut", duration: 0.1 }}
            />
            <motion.div
                className="fixed top-0 left-0 w-12 h-12 border border-white rounded-full pointer-events-none z-[100] mix-blend-difference"
                animate={{ x: mousePosition.x - 24, y: mousePosition.y - 24, scale: isHovering ? 1.5 : 1 }}
                transition={{ type: "tween", ease: "backOut", duration: 0.15 }}
            />
        </>
    );
}
